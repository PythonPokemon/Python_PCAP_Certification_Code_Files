"""
Die isalnum()-Methode
Die parameterlose Methode prüft, ob die Zeichenkette nur Ziffern oder alphabetische Zeichen (Buchstaben) enthält, 
und gibt entsprechend dem Ergebnis True oder False zurück.isalnum()
"""

# Demonstrating the isalnum() method:
print('lambda30'.isalnum())
print('lambda'.isalnum())
print('30'.isalnum())
print('@'.isalnum())
print('lambda_30'.isalnum())
print(''.isalnum())