"""
Die startswith()-Methode
Die Methode ist eine Spiegelreflexion von – sie prüft, ob eine gegebene Zeichenkette mit der angegebenen Teilzeichenkette beginnt.
    startswith()
    endswith()
"""

# Demonstrating the startswith() method:
print("omega".startswith("meg"))
print("omega".startswith("om"))

print()




"""
Die strip()-Methode
Die Methode kombiniert die Effekte, die durch und verursacht werden – 
sie erzeugt eine neue Zeichenkette, der alle führenden und hinterlaufenden Weißräume fehlen.
    strip()
    rstrip()
    lstrip()
"""

# Demonstrating the strip() method:
print("[" + "   aleph   ".strip() + "]")

