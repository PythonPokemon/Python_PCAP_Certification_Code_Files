"""
Die Swapcase()-Methode
Die Methode erstellt eine neue Zeichenkette, indem der Großfall aller Buchstaben innerhalb der Quellzeichenkette vertauscht wird: 
Kleinbuchstaben werden zu Großbuchstaben und umgekehrt.
"""
# Demonstrating the swapcase() method:
print("I know that I know nothing.".swapcase())
print()


"""
Die Title()-Methode
Die Methode erfüllt eine ähnliche Funktion – sie ändert den ersten Buchstaben jedes Wortes in Großbuchstaben, 
während alle anderen in Kleinbuchstaben umgewandelt werden.
"""
# Demonstrating the title() method:
print("I know that I know nothing. Part 1.".title())
print()



"""
Die Upper()-Methode
Zu guter Letzt erstellt die Methode eine Kopie der Quellzeichenkette, 
ersetzt alle Kleinbuchstaben durch ihre Großbuchstaben und gibt die Zeichenkette als Ergebnis zurück.
"""
# Demonstrating the upper() method:
print("I know that I know nothing. Part 2.".upper())



