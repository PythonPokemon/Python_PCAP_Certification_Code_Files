"""
Die Lower()-Methode
Die Methode erstellt eine Kopie einer Quellzeichenkette, 
ersetzt alle Großbuchstaben durch ihre kleinen Gegenstücke und gibt die Zeichenkette als Ergebnis zurück. 

Wenn die Zeichenkette keine Großbuchstaben enthält, gibt die Methode die ursprüngliche Zeichenkette zurück.

Hinweis: Die Methode berücksichtigt keine Parameter.lower()
"""


