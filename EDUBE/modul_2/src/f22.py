"""
Die islower()-Methode
Die Methode ist eine anspruchsvolle Variante – sie akzeptiert nur Kleinbuchstaben.
"""
# Example 1: Demonstrating the islower() method:
print("Moooo".islower())
print('moooo'.islower())

print("")




"""
Die isspace()-Methode
Die Methode identifiziert nur Weißräume – sie ignoriert alle anderen Zeichen (das Ergebnis ist dann).
"""
# Example 2: Demonstrating the isspace() method:
print(' \n '.isspace())
print(" ".isspace())
print("mooo mooo mooo".isspace())

print("")





"""
Die isupper()-Methode
Die Methode ist die Großbuchstabenversion – sie konzentriert sich nur auf Großbuchstaben.
"""
# Example 3: Demonstrating the isupper() method:
print("Moooo".isupper())
print('moooo'.isupper())
print('MOOOO'.isupper())
